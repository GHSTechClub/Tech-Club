                <h2 class="anchored"><a id="web-internet">The Web and the Internet</a></h2>
                <br />
                <p>To define the Web, we must first have a basic understanding of the Internet. People often use the two terms interchangeably, but let’s be clear: the Web and the Internet are two distinctly different technologies. The Internet, put simply, is a global network of networks that allows computers to communicate with one another over vast distances. Think of the Internet as an ocean, but instead of ships traveling across it in an exchange of goods, packets of information travel across it in an exchange of data. The Internet was designed to be a neutral platform for data exchange between computers. But like all platforms, it is useless without applications to make use of it... 
    </p>
    			<br />
                <p>That is where the Web comes in. The World Wide Web (or Web for short) is an incredibly popular and powerful application for the Internet (not the only one, though). It relies on the Internet as a medium for transporting documents called "web pages" and the requests that people have to make in order to view these documents. The Internet is complex and deserves its own in-depth explanation but for now we can think of it as an abstract medium that simply exists because the Web is a complex technology in its own right. We don’t need to further complicate things right now by worrying about how computers know where and how to send data and how to process incoming data and things like that. All we need to know is that they can do these things and do them reliably. By abstractifying the Internet, we can focus on how the Web uses it, rather than how it functions itself. 
</p>
